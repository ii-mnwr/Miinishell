#include "libft.h"

int	ft_contains(char *s, char c)
{
	size_t	i;

	if (!s || !s[0] || !c)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (1);
		i++;
	}
	return (0);
}
