#include "libft.h"

int	ft_isonlyspace(const char *s)
{
	size_t	i;

	if (!s || s[0] == '\0')
		return (0);
	i = 0;
	while (ft_isspace(s[i]))
		i++;
	return (i == ft_strlen(s));
}
