#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	dst_len;

	i = 0;
	while (dst[i] != '\0' && i < dstsize)
		i++;
	dst_len = i;
	j = 0;
	while (src[j] != '\0' && i + 1 < dstsize)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	if (dst_len < dstsize)
		dst[i] = '\0';
	return (dst_len + ft_strlen(src));
}
